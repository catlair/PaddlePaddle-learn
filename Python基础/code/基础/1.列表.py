t = [1, 2, 3, 4, 5]

print(t)
print(t[1])
print(t[-2])

# 连接
g = [6, 7]
print(t + g)

# 重复
print(g * 2)

# 修改
g[0] = 1
print(g)

# 这还能修改长 [1, 9, 6, 6, 7]
g[1:] = [9, 6, 6, 7]
print(g)

# 插入其中 [1, 2, 9, 6, 6, 7]
g[1:1] = [2]
print(g)

# 删除一段或一个 [1, 9, 6, 6, 7]
g[1:2] = []
print(g)

# 截取的第三个参数
d = ['你们好啊', '我是来自火星的', '水星人']

# 第三个参数是截取步长
# 0开始,2步一个 ['你们好啊', '水星人']
print(d[0::2])

# 从最后一个开始,截取步长为-1,就会颠倒
# ['水星人', '我是来自火星的', '你们好啊']
print(d[-1::-1])
# ['水星人', '你们好啊']
print(d[-1::-2])
